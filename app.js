let moonPhase = 'full';

switch (moonPhase) {
  case 'full':
    console.log('Howl!');
    break;
  case 'mostly full':
    console.log('Arms & Legs are getting harrier!');
    break;
  case 'mostly new':
    console.log('Back on two feet.');
    break;
  default:
    console.log('Invalid Moon Phase');
    break;
} 
